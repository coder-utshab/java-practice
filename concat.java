public class concat {

  public static void main(String[] args) {
    String firstName = "John ";
    String lastName = "Doe";
    System.out.println(firstName.concat(lastName));
  }
}


//concat() কী করে?
// concat() একটি String Method। এটি একটি String-এর শেষে আরেকটি String যোগ করে।

// কখন concat() ব্যবহার করব?
// concat() → শুধুমাত্র String-এর সাথে String যোগ করার জন্য।
// + → String-এর সাথে String, int, double, boolean সবকিছু যোগ করা যায়।